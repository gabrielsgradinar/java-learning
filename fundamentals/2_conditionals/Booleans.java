public class Booleans {
    public static void main(String[] args) {
        boolean bool1 = true;
        boolean bool2 = false;

        System.out.println("True: " + bool1 + ", False: " + bool2);

        System.out.println(5 < 10);

        String name = "Gabriel";
        String otherName = "Gabriel";

        System.out.println(name.equals(otherName));
    }
}